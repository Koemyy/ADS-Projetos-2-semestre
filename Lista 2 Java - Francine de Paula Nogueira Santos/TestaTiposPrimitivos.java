public class TestaTiposPrimitivos {
	
	public static void main (String[] args) {
		
		String n1 = "Cafe";
		byte n2 = 127;
		float n3 = 199.99f;
		boolean n4 = true;
		short n5 = 32767;
		float n6 = 987.12f;
		char n7 = 'I';
		long n8 = 123275267945l;
		double n9 = 7.123292653589793;
		int n10 = 9999999;
		
		System.out.println("String: " +n1);
		System.out.println("Byte: " +n2);
		System.out.println("Float: " +n3);
		System.out.println("Boolean: " +n4);
		System.out.println("Short: " +n5);
		System.out.println("Float: " +n6);
		System.out.println("Char: " +n7);
		System.out.println("Long: " +n8);
		System.out.println("Double: " +n9);
		System.out.println("Int: " +n10);
	}
}

